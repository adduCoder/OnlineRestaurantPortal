package com.restaurants.util;

/**
 * Enumeration representing the roles a user can have in the system.
 * <p>
 * This enum is used to differentiate between different types of users
 * in the application, such as general users and restaurant owners.
 * </p>
 */
public enum Role {
  /**
   * Role representing a general user of the application.
   */
  USER,
  /**
   * Role representing a restaurant owner in the application.
   */
  OWNER;
}
